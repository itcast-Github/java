package cn.itcast.sum;
/**
 * 
 * GetAvg类实现求取平均数功能
 * @author Apple
 * @version 1.6
 */
public class GetAvg {
	/**
	 * 
	 * @param a
	 * @param b
	 * @return avg
	 * 实现获取两个浮点数的平均数
	 * 调用方法,传递两个浮点数,返回平均值
	 */
	public static double getAvg(double a,double b){
		return (a+b)/2;
	}
}
